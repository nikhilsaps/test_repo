//this program shows various print function with datatypes in java
public class Main {
    public static void main(String[] args) {
        /*if you have seen the datatype 2_Datatype_in_java
        this program will be using that*/

        //for accessing the data you will not need or use "" in print function
        //first print is simple data print and 2nd one is print with statement
        boolean bool =true;
        System.out.println(bool);
        System.out.println("this is a boolean : "+bool);
        byte by =32;
        System.out.println(by);
        System.out.println("this is a byte : "+by);
        short sh = 64;
        System.out.println(sh);
        System.out.println("this is a short : "+sh);
        int in = 36;
        System.out.println(in);
        System.out.println("this is a int  : "+in);
        long lo = 32000;
        System.out.println(lo);
        System.out.println("this is a long : "+lo);
        float fl =2.3f;
        System.out.println(fl);
        System.out.println("this is a float : "+fl);
        double dou =2.345;
        System.out.println(dou);
        System.out.println("this is a double : "+dou);
        char ch = 'N';
        System.out.println(ch);
        System.out.println("this is a char : "+ch);




    }
}
